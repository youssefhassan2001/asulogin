$('#a').on('click', function () {
    $('#a').addClass('active').siblings().removeClass('active');
    $('#a').addClass('block').siblings().removeClass('block');
    $('#body').css('height', '45%');
    $('#login').css('display', 'block');
    $('#GraduatesAssociationServices').css('display', 'none');
    $('#EarlyAdmission').css('display', 'none');
    $('#FacultyServices').css('display', 'none');
    $('#CentersServices').css('display', 'none');
    $('#UniversityConference').css('display', 'none');
    $('#Application').css('display', 'none');
    $('#Blended').css('display', 'none');
    location.href ='#body'
    
});
